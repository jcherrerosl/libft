#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*ptr;

	len = ft_strlen(s1);
	ptr = malloc(len * sizeof(char));
	ft_memcpy(ptr, s1, len);
	return (ptr);
}
